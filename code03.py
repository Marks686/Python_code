# 变量命名的规则
# 1.变量名必须由数字 字母 下划线构成。
a1 = 20
# 不合法：a*a = 10
# 2.数字不能开头
# 不合法：1a = 20
# 3.变量名不能和Python的关键字重复
# 不合法：if = 20
# 4.Python中的变量名 区分大小写
num = 10
NUM = 10
#硬性规则 务必遵守！！


